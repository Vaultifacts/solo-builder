class TestTestGeneratorSyntax(unittest.TestCase):
    """Verify generated test code is valid Python."""

    def setUp(self):
        self.gen = SkeletonGenerator()

    def test_generates_valid_python_syntax(self):
        """Generated code should be compilable Python."""
        code = self.gen.generate_test(
            "Create a function to parse JSON strings",
            "",
            "utils/parser.py",
        )
        # Try to compile; should not raise SyntaxError
        try:
            compile(code, "<generated>", "exec")
        except SyntaxError as e:
            self.fail(f"Generated code has syntax error: {e}")

    def test_basic_description_generates_valid_code(self):
        """Even minimal description should generate valid code."""
        code = generate_test("function to add numbers")
        try:
            compile(code, "<generated>", "exec")
        except SyntaxError as e:
            self.fail(f"Syntax error: {e}")

    def test_empty_description_generates_valid_code(self):
        """Even empty description should generate valid code."""
        code = self.gen.generate_test("")
        try:
            compile(code, "<generated>", "exec")
        except SyntaxError as e:
            self.fail(f"Syntax error: {e}")

    def test_generated_code_contains_auto_comment(self):
        """Generated code should start with auto-generated comment."""
        code = self.gen.generate_test("test function")
        self.assertIn("auto-generated by TestGenerator", code)
        self.assertTrue(code.startswith("# auto-generated"))

    def test_generated_code_contains_imports(self):
        """Generated code should include unittest import."""
        code = self.gen.generate_test("test function")
        self.assertIn("import unittest", code)

    def test_generated_code_ends_with_newline(self):
        """Generated code should end with newline."""
        code = self.gen.generate_test("test function")
        self.assertTrue(code.endswith("\n"))


class TestTestGeneratorClassNaming(unittest.TestCase):
    """Verify test class naming follows conventions."""

    def setUp(self):
        self.gen = SkeletonGenerator()

    def test_class_name_starts_with_test(self):
        """Test class name should start with 'Test'."""
        code = self.gen.generate_test("Create function foo_bar")
        self.assertIn("class Test", code)

    def test_class_name_follows_camelcase_convention(self):
        """Test class names should use CamelCase after 'Test'."""
        code = self.gen.generate_test("Create function parser")
        self.assertIn("class TestParser(unittest.TestCase):", code)

    def test_function_name_inferred_for_class_name(self):
        """Class name should be inferred from function/class name in description."""
        code = self.gen.generate_test("def calculate_total(items): ...")
        # Should use first word: "calculate" → "TestCalculate"
        self.assertRegex(code, r'class Test\w+\(unittest\.TestCase\):')

    def test_sanitizes_special_chars_in_class_name(self):
        """Special characters should be removed from class name."""
        code = self.gen.generate_test("Create function parse-json-data")
        # Should not have hyphens, should be CamelCase
        self.assertRegex(code, r'class Test\w+\(unittest\.TestCase\):')
        self.assertNotIn("parse-json", code.lower())

    def test_method_names_follow_test_convention(self):
        """Test method names should start with 'test_'."""
        code = self.gen.generate_test("Create function process")
        # Count test_ methods
        import re
        methods = re.findall(r'def (test_\w+)\(self\):', code)
        self.assertGreaterEqual(len(methods), 2)

    def test_method_names_are_descriptive(self):
        """Test method names should describe what is being tested."""
        code = self.gen.generate_test("function to validate email")
        self.assertRegex(code, r'def test_\w+\(self\):')


