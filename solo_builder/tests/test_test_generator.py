class TestTestGeneratorIntegration(unittest.TestCase):
    """Integration tests for typical workflows."""

    def setUp(self):
        self.gen = SkeletonGenerator()

    def test_complete_function_test_generation(self):
        """Full workflow: description + output → complete test file."""
        description = "Create a validate_email function"
        output = """
def validate_email(email: str) -> bool:
    \"\"\"Check if email is valid.\"\"\"
    return "@" in email
        """
        module_path = "utils/validators.py"

        code = self.gen.generate_test(description, output, module_path)
        test_file = self.gen.suggest_test_file(module_path)

        # Verify code structure
        self.assertIn("class Test", code)
        self.assertIn("def test_", code)
        self.assertIn("import unittest", code)

        # Verify file path (normalize separators)
        test_file_normalized = test_file.replace("\\", "/")
        self.assertIn("validators", test_file_normalized)
        self.assertIn("tests/", test_file_normalized)

        # Code should be valid
        try:
            compile(code, "<generated>", "exec")
        except SyntaxError as e:
            self.fail(f"Integration test generated invalid syntax: {e}")

    def test_api_endpoint_complete_workflow(self):
        """Full workflow for API endpoint test generation."""
        description = "Add a GET /api/tasks endpoint to list all tasks"
        output = "Added GET /api/tasks\nStatus: 200 OK\nReturns: [task1, task2]"
        module_path = "api/app.py"

        code = self.gen.generate_test(description, output, module_path)
        test_file = self.gen.suggest_test_file(module_path)

        # Should generate endpoint-style tests
        self.assertIn("test_client", code)
        self.assertIn("def setUp", code)
        self.assertIn("status_code", code)

        # File path should be api/test_app.py (normalize separators)
        test_file_normalized = test_file.replace("\\", "/")
        self.assertEqual(test_file_normalized, "api/test_app.py")

    def test_cli_command_complete_workflow(self):
        """Full workflow for CLI command test generation."""
        description = "Add --dry-run flag to preview changes"
        output = "$ python cli.py --dry-run\npreview output"
        module_path = "solo_builder_cli.py"

        code = self.gen.generate_test(description, output, module_path)

        # Should include subprocess patterns
        self.assertIn("subprocess", code)
        self.assertIn("returncode", code)


