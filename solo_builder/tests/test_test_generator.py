class TestTestGeneratorFilePath(unittest.TestCase):
    """Test test file path suggestion."""

    def setUp(self):
        self.gen = SkeletonGenerator()

    def test_suggests_test_directory_for_utils(self):
        """Utils modules should suggest tests/ directory."""
        path = self.gen.suggest_test_file("utils/parser.py")
        # Normalize path separators
        path = path.replace("\\", "/")
        self.assertEqual(path, "tests/test_parser.py")

    def test_suggests_same_directory_for_api(self):
        """API modules should suggest same directory (e.g., api/test_app.py)."""
        path = self.gen.suggest_test_file("api/app.py")
        path = path.replace("\\", "/")
        self.assertEqual(path, "api/test_app.py")

    def test_suggests_same_directory_for_discord_bot(self):
        """Discord bot modules should stay in discord_bot/."""
        path = self.gen.suggest_test_file("discord_bot/bot.py")
        path = path.replace("\\", "/")
        self.assertEqual(path, "discord_bot/test_bot.py")

    def test_avoids_double_test_prefix(self):
        """Should not create test_test_ names."""
        path = self.gen.suggest_test_file("api/test_app.py")
        path = path.replace("\\", "/")
        self.assertEqual(path, "api/test_app.py")
        self.assertNotIn("test_test_", path)

    def test_converts_py_extension(self):
        """Should handle .py extension properly."""
        path = self.gen.suggest_test_file("utils/helper.py")
        self.assertTrue(path.endswith(".py"))
        self.assertNotIn(".py.py", path)

    def test_handles_empty_module_path(self):
        """Should handle empty module path gracefully."""
        path = self.gen.suggest_test_file("")
        self.assertTrue(path.endswith(".py"))
        self.assertIn("test_", path)

    def test_handles_none_module_path(self):
        """Should handle None module path gracefully."""
        path = self.gen.suggest_test_file(None)
        self.assertTrue(path.endswith(".py"))

    def test_extracts_filename_from_deep_path(self):
        """Should extract filename from deep directory paths."""
        path = self.gen.suggest_test_file("src/lib/utils/math_helper.py")
        self.assertIn("test_math_helper.py", path)


